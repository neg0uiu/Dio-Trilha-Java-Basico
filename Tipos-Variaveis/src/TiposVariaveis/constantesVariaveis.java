package tiposvariaveis;

public class constantesVariaveis {
    public static void main(String[] args) {
        
        //isso eh uma VARIAVEL
        // (tipo) (nome) = (valor);
            //exemplo:
            int inteiro = 5;

        //isso eh uma CONSTANTE (sempre com "final" antes do tipo e o nome com caixa alta)
        // final (tipo) (NOME) = (valor);
            //exemplo:
            final int INTEIRO = 7;


        System.out.println("variavel: " + inteiro);
        System.out.println("CONSTANTE: " + INTEIRO);


    }
}
